/**
 * GFM Export System
 * CSV, JSON, and printable HTML export for all data tables.
 */

export function exportCSV(rows: Record<string,any>[], filename: string) {
  if (!rows.length) return;
  const headers = Object.keys(rows[0]);
  const csv = [
    headers.join(','),
    ...rows.map(r => headers.map(h => {
      const v = r[h];
      const s = String(v ?? '').replace(/"/g, '""');
      return s.includes(',') || s.includes('\n') ? `"${s}"` : s;
    }).join(','))
  ].join('\n');
  downloadBlob(csv, `${filename}.csv`, 'text/csv');
}

export function exportJSON(data: any, filename: string) {
  const json = JSON.stringify(data, null, 2);
  downloadBlob(json, `${filename}.json`, 'application/json');
}

