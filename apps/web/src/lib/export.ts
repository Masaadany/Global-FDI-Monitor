function downloadBlob(content: string, filename: string, mimeType: string) {
  const blob = new Blob([content], { type: mimeType });
  const url  = URL.createObjectURL(blob);
  const a    = document.createElement('a');
  a.href = url; a.download = filename;
  document.body.appendChild(a); a.click();
  document.body.removeChild(a); URL.revokeObjectURL(url);
}

/** Export GFR table */
export function exportGFR(rankings: any[]) {
  exportCSV(rankings.map(r => ({
    Rank: r.rank, ISO3: r.iso3, Economy: r.name, Region: r.region,
    Income: r.income, Composite: r.composite, Tier: r.tier,
    Macro: r.macro, Policy: r.policy, Digital: r.digital,
    Human: r.human, Infrastructure: r.infra, Sustainability: r.sustain,
    'GDP $B': r.gdp_b, 'FDI Inflows $B': r.fdi_b,
  })), `GFR_Rankings_Q1_2026_${new Date().toISOString().slice(0,10)}`);
}

/** Export signals table */
export function exportSignals(signals: any[]) {
  exportCSV(signals.map(s => ({
    'Reference Code': s.id || s.reference_code,
    Grade: s.grade, Company: s.company, HQ: s.hq,
    Economy: s.economy, 'ISO3': s.iso3,
    'ISIC Sector': s.sector, 'CapEx USD': s.capex_usd,
    'SCI Score': s.sci_score, Type: s.signal_type,
    Status: s.status, Date: s.signal_date,
    'Provenance Source': s.provenance?.source || '',
    'Verified': s.provenance?.verified ? 'Yes' : 'No',
  })), `GFM_Signals_${new Date().toISOString().slice(0,10)}`);
}

/** Export pipeline deals */
export function exportPipeline(deals: any[]) {
  exportCSV(deals.map(d => ({
    ID: d.id, Company: d.company, HQ: d.hq,
    Economy: d.economy || d.iso3, Sector: d.sector,
    'CapEx $M': d.capex_m, Stage: d.stage,
    'Probability %': d.probability, Contact: d.contact,
    'Days in Stage': d.days_in_stage, Notes: d.notes,
  })), `GFM_Pipeline_${new Date().toISOString().slice(0,10)}`);
}
